static void BM_AnalyzePacket(benchmark::State& state) {
    HttpAnalyzer analyzer;
    for (auto _ : state) {
        analyzer.analyzePacket(exampleHttpPacket, sizeof(exampleHttpPacket));
    }
}
// Register the function as a benchmark
